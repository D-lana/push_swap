#include "checker.h"

void	ft_check_instructions(const char *s, int i)
{
	if ((s[i] == 's' && s[i - 1] == 's') || (s[i] == 'r' && s[i - 1] == 'r'))
	{
		if (((s[i - 2] == '\0' || s[i - 2] == '\n') && i > 1) || i == 1)
			return ;
	}
	if (s[i] == 'a' || s[i] == 'b' || s[i] == 'r')
	{
		i--;
		if ((s[i] == 'r' && s[i - 1] == 'r'))
		{
			if (((s[i - 2] == '\0' || s[i - 2] == '\n') && i > 1) || i == 1)
				return ;
		}
		if ((s[i] == 's' || s[i] == 'r' || s[i] == 'p') && s[i + 1] != 'r')
		{
			if (((s[i - 1] == '\0' || s[i - 1] == '\n') && i > 0) || i == 0)
				return ;
		}
	}
	ft_error();
}
